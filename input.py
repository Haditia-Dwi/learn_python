## PENGGUNAAN INPUT
nama = input("Masukkan nama anda")
print("Hello", nama)

umur = int(input("Berapa Umurmu:"))
print(f"Umurku adalah {umur}tahun")

## peraturan konversi tipe data dalam penggunaan function
## gunakan int(input) untuk mengubah nilai menjadi integer
## gunakan str() untuk mengubah nilai menjadi string atau teks
## gunakan float() untuk mengubah string atau integer menjadi float/desimal
## gunakan bool() untuk mengubah data lain menjadi boolean(true/false)